    private void cargarInformacionAlojamiento(Hospedaje hospedajes) {
        try {
            txtTipo.setText(hospedajes.getTipo());
            txtTipo.setEnabled(false);
            txtTipoAlojamiento.setText(hospedajes.getCategoria());
            txtTipoAlojamiento.setEnabled(false);
            spnCantidadPersonas.setValue(Integer.parseInt(hospedajes.getCantidadpersonas()));
            spnCantidadPersonas.setEnabled(false);
            spnBano.setValue(Integer.parseInt(hospedajes.getBano()));
            spnBano.setEnabled(false);
            txtUbicacion.setText(hospedajes.getUbicacion());
            txtUbicacion.setEnabled(false);
            spnHabitaciones.setValue(Integer.parseInt(hospedajes.getHabitaciones()));
            spnHabitaciones.setEnabled(false);
            spnCamas.setValue(Integer.parseInt(hospedajes.getCamas()));
            spnCamas.setEnabled(false);
            txtValorNoche.setText(hospedajes.getValorPorNoche());
            txtValorNoche.setEnabled(false);
            txtDescripcion.setText(hospedajes.getServicios());
            txtDescripcion.setEnabled(false);
            lblmagen.setIcon(new ImageIcon(controlador.cargarImagenBufferedImage(hospedajes.getImagen())));
        } catch (CargarImagenException ex) {
            imprimir(ex.getMessage());
        }

    }

    private void asignarFechaHoy() {

        Calendar hoy = new GregorianCalendar();
        dateFechaHoy.setCalendar(hoy);
        fechaMinima();
    }

    private void fechaMinima() {
        Date date = new Date();
        dateFechaLlegada.setMinSelectableDate(date);
        dateFechaSalida.setMinSelectableDate(date);

    }

