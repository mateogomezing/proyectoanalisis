    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        try {
            String categoria = controlador.obtenerDatoJComboBox(cboCategoria);
            String nombre = controlador.obtenerDatoJtextFile(txtNombreCompleto);
            String descripcion = controlador.obtenerDatoJtextArea(txtDescripcion);
            controlador.modificar(categoria, nombre, descripcion);
            JOptionPane.showMessageDialog(null, "Se modifico el tipo alojamiento" + nombre + " correctamente");
            vaciarCampos();
            listar();
            btnRegistrar.setEnabled(true);
            btnModificar.setEnabled(false);
            btnBuscar.setEnabled(true);
            btnEliminar.setEnabled(false);
            cboCategoria.setEnabled(true);
            btnCancelar.setEnabled(false);
        } catch (BuscarTipoAlojamientoException | ModificarTipoAlojamientoException | NombreCategoriaException | DatosIncompletosException ex) {

            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {
            String categoria = controlador.obtenerDatoJComboBox(cboCategoria);
            String nombre = controlador.obtenerDatoJtextFile(txtNombreCompleto);
            String descripcion = controlador.obtenerDatoJtextArea(txtDescripcion);
            controlador.guardarTipoAlojamiento(categoria, nombre, descripcion);
            JOptionPane.showMessageDialog(null, "Se guardó el tipo alojamiento" + nombre + " correctamente");
            vaciarCampos();
            listar();
        } catch (CategoriaException | GuardarTipoAlojamientoException | DatosIncompletosException | NombreCategoriaException ex) {

            JOptionPane.showMessageDialog(null, ex.getMessage());
        }
    }//GEN-LAST:event_btnRegistrarActionPerformed

