    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
        try {
            String opcion = CbxFiltrar.getSelectedItem().toString();
            String accion = controlador.obtenerDatoJtextFile(txtFiltrar);
            tblLista.setModel(controlador.filtrar(opcion, accion));
        } catch (ComboBoxException | DatosIncompletosException e) {
            imprimir(e.getMessage());
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        try {
            TipoAlojamiento tipoalojamiento = controlador.buscarTipoAlojamiento(controlador.obtenerDatoJtextFile(txtNombreCompleto));
            imprimir("Se encontro el tipo Alojamiento " + tipoalojamiento.getNombre() + " correctamente");
            cargarInformacion(tipoalojamiento);
            btnRegistrar.setEnabled(false);
            btnEliminar.setEnabled(true);
            btnModificar.setEnabled(true);
            btnBuscar.setEnabled(false);
            cboCategoria.setEnabled(false);
            btnCancelar.setEnabled(true);
        } catch (DatosIncompletosException | BuscarTipoAlojamientoException ex) {
            imprimir(ex.getMessage());
        }
    }//GEN-LAST:event_btnBuscarActionPerformed

